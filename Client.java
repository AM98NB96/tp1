/**
 * La classe client contient les informations du client.
 *
 * @param nom 
 * 			Le nom du client.
 */
public class Client {
	
	String nom;
	
	/**
	 * Constructeur de la classe client.
	 * 
	 * @param nom
	 * 			Le nom du client.
	 */
	public Client() {}
	
	public Client(String nom) {
		this.nom = nom;
	}
}